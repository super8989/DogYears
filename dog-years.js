const myAge = 29;

let earlyYears = 2;
earlyYears *= 10.5;

let laterYears = myAge - 2;
laterYears *= 4;

const myAgeinDogYears = earlyYears + laterYears;

var myName = 'SAM'.toLowerCase();


console.log(`My name is ${myName}. I am ${myAgeinDogYears} years old in dog years.`);

console.log('Alternatively... My name is ' + myName + '.' + ' I am ' + myAgeinDogYears + ' years old in dog years.');
